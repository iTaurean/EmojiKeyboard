    /**
     * 根据内容获取emotion对象
     *
     * @param content
     * @return
     */
    public static EmoticonBean getEmoticonBean(String content) {
        EmoticonBean emoticonBean = null;
        for (EmoticonBean bean : EMOTIONS) {
            if (bean.getContent().trim().equals(content.trim())) {
                emoticonBean = bean;
                break;
            }
        }
        return emoticonBean;
    }

